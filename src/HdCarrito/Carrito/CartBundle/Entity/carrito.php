<?php

namespace HdCarrito\Carrito\CartBundle\Entity;

/**
 * carrito
 */
class carrito
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $estatus;

    /**
     * @var string
     */
    private $llave;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set estatus
     *
     * @param string $estatus
     *
     * @return carrito
     */
    public function setEstatus($estatus)
    {
        $this->estatus = $estatus;

        return $this;
    }

    /**
     * Get estatus
     *
     * @return string
     */
    public function getEstatus()
    {
        return $this->estatus;
    }

    /**
     * Set llave
     *
     * @param string $llave
     *
     * @return carrito
     */
    public function setLlave($llave)
    {
        $this->llave = $llave;

        return $this;
    }

    /**
     * Get llave
     *
     * @return string
     */
    public function getLlave()
    {
        return $this->llave;
    }
}
